    private void loginbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbuttonActionPerformed
    String username = user.getText().trim();
    String passwordInput = password.getText().trim();

    try {
        if (username.equals("admin") && passwordInput.equals("admin")) {
            logger.logInfo("Admin logged in successfully.");
            User admin = UserFactory.createUser("admin");
            System.out.println("Welcome " + admin.getRole());
            setVisible(false);
            new Dashboard().setVisible(true); // Pass the role dynamically

        } else {
            Connection con = ConnectionProvider.getInstance().getConnection();
            Statement st = con.createStatement();
            String query = "SELECT * FROM student WHERE name='" + username + "' AND studentID='" + passwordInput + "'";
            ResultSet rs = st.executeQuery(query);

            if (rs.next()) {
                logger.logInfo("Student logged in successfully. Username: " + username);
                User regularUser = UserFactory.createUser("regular");
                System.out.println("Welcome " + regularUser.getRole());
                setVisible(false);
                new StudentDashboard().setVisible(true);
            } else {
                logger.logError("Invalid login attempt. Username: " + username);
                JOptionPane.showMessageDialog(null, "Incorrect username, student ID");
            }
        }
    } catch (Exception e) {
        logger.logError("Error during login attempt: " + e.getMessage());
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_loginbuttonActionPerformed

